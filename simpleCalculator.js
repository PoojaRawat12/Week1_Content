let x = 10;
let y = 5;
let operator = "+";
if (operator === "+") {
  console.log(x + y);
} else if (operator === "-") {
  console.log(x - y);
} else if (operator === "*") {
  console.log(x * y);
} else if (operator === "/") {
  console.log(x / y);
} else {
  console.log("Invalid operator");
}